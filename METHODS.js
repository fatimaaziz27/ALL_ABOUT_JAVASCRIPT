// What is a Method?
// A method is a function that belongs to an object.
// Syntax
// object.methodName();

// Example
const person = {
  greet() {
    console.log("Hello!");
  }
};
person.greet();


// String Methods
// Syntax
// string.methodName();

// Example
let text = "JavaScript";
text.toUpperCase();


// Array Methods
// Syntax
// array.methodName();

// Example
let numbers = [1, 2, 3];
numbers.push(4);


// Object Methods
// Syntax
// Object.methodName(object);

// Example
const student = {
  name: "Ali",
  age: 20
};
Object.keys(student);


// Math Methods
// Syntax
// Math.methodName();

// Example
Math.sqrt(64);


// Date Methods
// Syntax
// date.methodName();

// Example
const today = new Date();
today.getFullYear();
